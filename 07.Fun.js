//In JavaScript, a function is a reusable block of code that performs a specific task. Functions allow you to organize your code better and avoid repetition.

//Fun Declaration
function greet() {
  console.log("Hello!");
}
greet(); // Call the function


//Fun with Parameters

function greetUser(name) {
  console.log("Hello, " + name + "!");
}
greetUser("Saikiran");



//Fun with return Value
function add(a, b) {
  return a + b;
}
let sum = add(5, 3);
console.log(sum); // 8
